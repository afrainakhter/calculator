    private void sqroot(double num1) {

        t1.setText(Double.toString(Math.sqrt(num1)));

    }

    private void square(double num1) {
        t1.setText(Double.toString(num1 * num1));

    }

    private void add(double num1, double num2) {
        res(num1 + num2);

    }

    private void mal(double num1, double num2) {
        res(num1 * num2);

    }

    private void sub(double num1, double num2) {
        res(num1 - num2);

    }

    private void div(double num1, double num2) {
        res(num1 / num2);

    }

    private void res(double result) {

        t1.setText(Double.toString(result));
        jLable.setText("");
    }

